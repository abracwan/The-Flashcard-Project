usage_text = """
The Flashcard Project (FP) application can be started without card databases by running:

fp.exe 

It opens to a blank card that can be edited by typing a question and an answer. To begin making a new card deck, type in a question and an answer and click “Save”. A new card database will be generated named “cards.db”. Use the file menu to open any existing card database.

The green arrow button displays the next card.

Additional cards can be added be clicking “Add”, entering or pasting in the content, and clicking “Save”. Use “File-Save As” to store the file with a different name.

Cards will be displayed randomly by default. Use “Setup” to view them sequentially. When studying, formulate the answer before clicking “Answer”. If you do not want to see the card again, click “Archive”. To see these cards again you will need to reset the deck using the “Setup” menu item.

Share your cards by using the application to export them to a YAML file. This file type is both human and machine readable. The person you share the cards with can import your YAML file to a black card deck and study. They will also be able to import them to another card file and save all the cards as a single consolidated card deck.
"""

about_text = """
The Flashcard Project (FP) makes, manages, and facilitates viewing flashcards on a desktop. The native Windows interface and size-adjustable frame makes it easy to have other applications and study references displayed. This can be useful when studying a computer language so you can switch to a code editor or interpreter for testing and practice.

While other flashcard applications provide extended features, this offers a simple system that allows content sharing and merging. Cards can be categorized with tags and you can save your card decks. The files can be shared for use by anyone with a FP application.

With FP you can…

- Edit cards on the same screen as they are displayed.
- Save card decks to a database file.
- Export files in machine and human-readable formats.
- Merge card decks.
- Tag cards to allow filtering.
- Delete unwanted tags simultaneously from all cards.
- Display cards randomly or sequentially.
- Flag cards as archived so they will not be shown.
- Reset the deck so all archived and excluded cards are shown.

You can edit, add, and delete cards while you are studying. If there is a card you do not want to see again, click the “Archive” button. The deck can be reset at any time to return the status of all archived card to active. Otherwise, the deck will be automatically reset once all cards are viewed.

FP provides a way for multiple people to contribute when generating a card deck. To share a card deck, the card data can be exported to a human-readable YAML file. These files can be shared and then imported by someone else. 

Use the FP application to help with learning for any subject. If you are an instructor your students could be tasked with producing their own flashcards and submit them as card files. Their cards can be easily consolidated into a single card deck file that is a useful learning asset for all of students. 

The Flashcard Project is free and open source software written in Python. It can be used, redistributed, and modified it under the terms of its MIT license.
"""
